CREATE TABLE opu_stats (
  opu_stat_id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  team_member_id  INTEGER REFERENCES team_members(team_member_id) ON DELETE CASCADE NOT NULL,
  opu_date date NOT NULL DEFAULT now(),
  opu_total NUMERIC NOT NULL DEFAULT 0,
  opu_percent NUMERIC NOT NULL DEFAULT 0,
  opu_inf NUMERIC NOT NULL DEFAULT 0
);