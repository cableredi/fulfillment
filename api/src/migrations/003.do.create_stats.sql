CREATE TABLE stats (
  stat_id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  team_member_id  INTEGER REFERENCES team_members(team_member_id) ON DELETE CASCADE NOT NULL,
  stat_type TEXT NOT NULL,
  stat_date DATE NOT NULL DEFAULT now(),
  total NUMERIC NOT NULL DEFAULT 0,
  percent NUMERIC NOT NULL DEFAULT 0,
  inf NUMERIC NULL DEFAULT 0
);